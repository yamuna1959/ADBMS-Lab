AIM:TO CALCULATE ELECTRICITY BILL USING CURSORS

CREATE TABLE EBILL(EBNO NUMBER(5),NAME VARCHAR(15),UNITS NUMBER(4),CHARGES NUMBER(3));
INSERT INTO EBILL(EBNO,NAME,UNITS)VALUES(:EBNO,:NAME,:UNITS);
SELECT * FROM EBILL;
 

DECLARE
CURSOR CUR IS SELECT * FROM EBILL;
BEGIN
FOR I IN CUR
LOOP
IF I.UNITS<=100 THEN
UPDATE EBILL SET CHARGES=I.UNITS*1 WHERE EBNO=I.EBNO;
ELSIF I.UNITS>100 AND I.UNITS<=200 THEN
UPDATE EBILL SET CHARGES=I.UNITS*2 WHERE EBNO=I.EBNO;
ELSE
UPDATE EBILL SET CHARGES=I.UNITS*3 WHERE EBNO=I.EBNO;
END IF;
END LOOP;
END;

SELECT * FROM EBILL;
 
